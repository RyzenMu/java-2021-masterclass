public class SwapTwoIntegers {
    public static void main(String[] args) {
        int a = 10;
        int b = 100;
        int temp;
        temp = a;
        a = b;
        b = temp;
        System.out.println("The value of a is " + a);
        System.out.println("The value of b is " + b);
    }
}
