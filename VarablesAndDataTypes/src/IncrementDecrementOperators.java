public class IncrementDecrementOperators {
    public static void main(String[] args) {
        int i = 9;
        System.out.println(i++);
        System.out.println(++i);
        i+=5;
        System.out.println(i);
        i--;
        System.out.println(i);
        i/=2;
        System.out.println(i);
    }
}
