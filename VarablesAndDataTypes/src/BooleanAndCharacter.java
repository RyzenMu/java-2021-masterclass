public class BooleanAndCharacter {
    public static void main(String[] args) {
        /*
        * char - text - 2 byte
        * boolean - bool - true or false - 1 byte
        * */

        char c = 4;
        System.out.println(c);
        char c1 = 65;
        System.out.println(c1); // prints 'A'
        char c2 = 77;
        System.out.println(c2);

        boolean b = true;
        System.out.println(b);
    }
}
