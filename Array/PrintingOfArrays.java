public class PrintingOfArrays {
    public static void main(String[] args) {

        // printing using for each
        Double [] da = {4.6, 2.8, 6.3, 9.8};
        for(double d : da){
            System.out.println(d);
        }
        System.out.println("*********************");
        //printing using for each 2
        String [] s = {"Hey,", "Hello", "Come Again", "go"};
        for(String q : s){
            System.out.println(q);
        }
    }
}
